    public function getCities(){
        //
        $cities = City::with('country')->get();

        return response()->json([
            'data' => $this->transformCollection($cities)
        ], 200);
    }

    private function transformCollection($cities){
        return array_map([$this, 'transform'], $cities->toArray());
    }

    private function transform($cities){
        return [
            'city_id' => $cities['id'],
            'city_name' => $cities['name'],
            'country_name' => $cities['country']['name'],
        ];
    }
